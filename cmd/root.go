package cmd

import (
	"fmt"
	"os"

	homedir "github.com/mitchellh/go-homedir"
	"github.com/spf13/cobra"
	"github.com/spf13/viper"
)

var cfgFile string

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "neke",
	Short: "A simple struct/interface/enum generator for Go and TypeScript",
	Long: `Neke is a simple code generator for shifting data between Go and TypeScript using JSON.

It generates code out of Neke files which look very similar to protobuf/gRPC files.
It's unique feature is that whenever something has a type, you can give a type for each language.
For example:

    struct Foo {
        required bar go:int32 ts:number
        optional baz common:string
    }

would result in 'bar' having int32 in Go and number in Typescript.

Other than structs Neke also supports enums and interfaces.
Enums are simple value lists:

    enum MyEnum {
        foo,
        bar,
        baz
    }

Interfaces consist of functions with one argument and one return value each:

    interface DoesSomething {
        func something(common:SomethingReq) returns (go:bool ts:boolean)
        func anotherThing(common:AnotherThingReq) returns (common:AnotherThingResp)
    }
`,
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

func init() {
	cobra.OnInitialize(initConfig)

	// Here you will define your flags and configuration settings.
	// Cobra supports persistent flags, which, if defined here,
	// will be global for your application.
	rootCmd.PersistentFlags().StringVar(&cfgFile, "config", "", "config file (default is $HOME/.neke.yaml)")

	// Cobra also supports local flags, which will only run
	// when this action is called directly.
	rootCmd.Flags().BoolP("toggle", "t", false, "Help message for toggle")
}

// initConfig reads in config file and ENV variables if set.
func initConfig() {
	if cfgFile != "" {
		// Use config file from the flag.
		viper.SetConfigFile(cfgFile)
	} else {
		// Find home directory.
		home, err := homedir.Dir()
		if err != nil {
			fmt.Println(err)
			os.Exit(1)
		}

		// Search config in home directory with name ".neke" (without extension).
		viper.AddConfigPath(home)
		viper.SetConfigName(".neke")
	}

	viper.AutomaticEnv() // read in environment variables that match

	// If a config file is found, read it in.
	if err := viper.ReadInConfig(); err == nil {
		fmt.Println("Using config file:", viper.ConfigFileUsed())
	}
}
